<?php

class Operaciones {

    public function __constructor(){

    }

    public function sum($n1, $n2){
        return $n1 + $n2;
    }

    public function resta($n1, $n2){
        return $n1 - $n2;
    }

    public function division($n1, $n2){
        return $n1 / $n2;
    }

    public function multi($n1, $n2){
        return $n1 * $n2;
    }
}